"""Spike: does query-side expansion close the query->chunk semantic gap?

The diagnostic from the hybrid spike: dense@20 = 0.46 — even a 20-deep dense net
misses the gold 54% of the time, because eval queries are farmer-slang paraphrases
whose embedding lands far from the answering chunk. Two query-side fixes, both
generated by the LOCAL Qwen-7B (zero Groq quota):

  - rewrite: slang question -> concise formal agricultural search query
  - HyDE:    generate a hypothetical extension-doc answer, embed THAT

Compares raw vs rewrite vs HyDE on the same dense retrieval path + index.

Run: EMBEDDING_MODEL_PATH=thenlper/gte-base PINECONE_INDEX_NAME=agroar-prod-gte-v2 \
       python evals/eval_query_expansion.py --sample 100
"""
import argparse
import json
import os
from pathlib import Path
from dotenv import load_dotenv

load_dotenv(Path(__file__).parent.parent / ".env")

from sentence_transformers import SentenceTransformer
from pinecone import Pinecone

from local_llm import _gen

MODEL_NAME = os.environ.get("EMBEDDING_MODEL_PATH", "thenlper/gte-base")
INDEX_NAME = os.environ.get("PINECONE_INDEX_NAME", "agroar-prod-gte-v2")
DENSE_K = 20
TOP_K = 5

REWRITE_SYS = (
    "You rewrite an Arkansas farmer's casual question into a concise, formal "
    "agricultural search query using precise extension terminology (crop names, "
    "agronomic terms, product/chemical categories). Output ONLY the rewritten "
    "query, one line, no preamble."
)
HYDE_SYS = (
    "You are an Arkansas agricultural extension document. Write 2-3 factual "
    "sentences that would directly answer the farmer's question, using precise "
    "agronomic terminology, product names, and rates where relevant. Output ONLY "
    "the passage."
)


def rewrite(q: str) -> str:
    return _gen(REWRITE_SYS, q, 60).strip()


def hyde(q: str) -> str:
    return _gen(HYDE_SYS, q, 160).strip()


def _metrics(ranked_ids, gold, k=TOP_K):
    hit1 = 1.0 if ranked_ids[:1] == [gold] else 0.0
    hit5 = 1.0 if gold in ranked_ids[:k] else 0.0
    mrr = 0.0
    for rank, rid in enumerate(ranked_ids[:k], start=1):
        if rid == gold:
            mrr = 1.0 / rank
            break
    recall20 = 1.0 if gold in ranked_ids[:DENSE_K] else 0.0
    return hit1, hit5, mrr, recall20


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--eval-set", type=Path,
                    default=Path(__file__).parent / "eval_set_v2_remap.jsonl")
    ap.add_argument("--sample", type=int, default=100)
    args = ap.parse_args()

    items = [json.loads(l) for l in open(args.eval_set, encoding="utf-8")][:args.sample]
    print(f"Items: {len(items)} | model: {MODEL_NAME} | index: {INDEX_NAME}")

    model = SentenceTransformer(MODEL_NAME)
    index = Pinecone(api_key=os.environ["PINECONE_API_KEY"]).Index(INDEX_NAME)

    variants = ("raw", "rewrite", "hyde")
    agg = {v: [0.0, 0.0, 0.0, 0.0] for v in variants}
    n = len(items)
    for i, it in enumerate(items):
        q, ns, gold = it["query"], it["namespace"], it["chunk_id"]
        texts = {"raw": q, "rewrite": rewrite(q), "hyde": hyde(q)}
        if i < 2:
            print(f"\n[sample {i}] raw: {q}")
            print(f"          rewrite: {texts['rewrite']}")
            print(f"          hyde: {texts['hyde'][:160]}")
        for v in variants:
            emb = model.encode(texts[v], normalize_embeddings=True).tolist()
            res = index.query(vector=emb, top_k=DENSE_K, namespace=ns, include_values=False)
            ids = [m["id"] for m in res.get("matches", [])]
            for j, val in enumerate(_metrics(ids, gold)):
                agg[v][j] += val
        if (i + 1) % 25 == 0:
            print(f"  {i+1}/{n}")

    print("\n=== QUERY EXPANSION: raw vs rewrite vs HyDE (dense, same index) ===")
    print(f"{'':10} {'hit@1':>8} {'hit@5':>8} {'MRR@5':>8} {'recall@20':>10}")
    for v in variants:
        h1, h5, mrr, r20 = (x / n for x in agg[v])
        print(f"{v:10} {h1:8.3f} {h5:8.3f} {mrr:8.3f} {r20:10.3f}")


if __name__ == "__main__":
    main()
